// Создаем пустой объект для хранения пользователей
let users = {};

// Цикл от 1 до 10
for (let i = 1; i <= 10; i++) {
    // Запрашиваем имя и возраст пользователя
    let name = prompt(`Введите имя пользователя ${i}:`);
    let age = prompt(`Введите возраст пользователя ${i}:`);

    // Записываем данные в объект users
    users[i] = {
        name: name,
        age: age
    };
}

// Выводим информацию о каждом пользователе в консоль
for (let id in users) {
    console.log(`Пользователь - ${id}`);
    console.log(`Имя - ${users[id].name}`);
    console.log(`Возраст - ${users[id].age}`);
    console.log('------------------');
}

// Выводим весь объект в консоль целиком
console.log(users);
